// Мини-парсер markdown (заголовки, списки, таблицы, **жирный**, *курсив*, абзацы)
window.renderMarkdown = function(md) {
  const lines = md.split("\n");
  const out = [];
  let i = 0;
  const inline = (s) => s
    .replace(/\*\*(.+?)\*\*/g, "<strong>$1</strong>")
    .replace(/\*(.+?)\*/g, "<em>$1</em>")
    .replace(/`(.+?)`/g, "<code>$1</code>")
    .replace(/\[(.+?)\]\((.+?)\)/g, '<a href="$2">$1</a>');
  while (i < lines.length) {
    const ln = lines[i];
    if (/^#{1,6}\s/.test(ln)) {
      const lvl = ln.match(/^(#+)/)[1].length;
      out.push(`<h${lvl + 1}>${inline(ln.replace(/^#+\s*/, ""))}</h${lvl + 1}>`);
      i++;
    } else if (/^\|/.test(ln) && /^\|/.test(lines[i+1] || "")) {
      // table
      const rows = [];
      while (i < lines.length && /^\|/.test(lines[i])) { rows.push(lines[i]); i++; }
      if (rows.length >= 2) {
        const head = rows[0].split("|").slice(1, -1).map(s => s.trim());
        const body = rows.slice(2).map(r => r.split("|").slice(1, -1).map(s => s.trim()));
        out.push(`<table class="md-table"><thead><tr>${head.map(h => `<th>${inline(h)}</th>`).join("")}</tr></thead><tbody>${body.map(r => `<tr>${r.map(c => `<td>${inline(c)}</td>`).join("")}</tr>`).join("")}</tbody></table>`);
      }
    } else if (/^[-*]\s/.test(ln)) {
      const items = [];
      while (i < lines.length && /^[-*]\s/.test(lines[i])) {
        items.push(`<li>${inline(lines[i].replace(/^[-*]\s+/, ""))}</li>`);
        i++;
      }
      out.push(`<ul>${items.join("")}</ul>`);
    } else if (/^\d+\.\s/.test(ln)) {
      const items = [];
      while (i < lines.length && /^\d+\.\s/.test(lines[i])) {
        items.push(`<li>${inline(lines[i].replace(/^\d+\.\s+/, ""))}</li>`);
        i++;
      }
      out.push(`<ol>${items.join("")}</ol>`);
    } else if (ln.trim() === "") {
      i++;
    } else {
      let p = ln;
      i++;
      while (i < lines.length && lines[i].trim() !== "" && !/^[#\-*\d|]/.test(lines[i])) {
        p += " " + lines[i]; i++;
      }
      out.push(`<p>${inline(p)}</p>`);
    }
  }
  return out.join("\n");
};

