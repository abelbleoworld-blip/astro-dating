# Склонение Coal Sack в разные эпохи
def ecl_to_dec(ecl_lon, ecl_lat, obliquity):
    eps = np.radians(obliquity)
    lam = np.radians(ecl_lon)
    bet = np.radians(ecl_lat)
    sin_dec = np.sin(bet)*np.cos(eps) + np.cos(bet)*np.sin(eps)*np.sin(lam)
    return np.degrees(np.arcsin(np.clip(sin_dec, -1, 1)))

def max_altitude(dec, lat):
    if dec > lat:
        return 90 - (dec - lat)
    else:
        return 90 - (lat - dec)

