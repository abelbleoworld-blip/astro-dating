"""P1: Ласко (~17 000 лет назад) — прецессионная датировка «звёздного быка».
Шесть точек = Плеяды? Бык = Телец? Глаз = Альдебаран?"""

import numpy as np
from pathlib import Path
from datetime import datetime

OUT = Path(__file__).resolve().parent.parent / "results" / "p1_lascaux.md"

# Ласко: 45.05°N, 1.17°E
LAT = 45.05
LON = 1.17

PRECESSION = 50.29 / 3600  # °/год
OBLIQUITY_NOW = 23.44

# Ключевые звёзды в J2000
# Эклиптическая долгота (тропическая)
PLEIADES_LON = 60.0    # η Tau (Alcyone)
ALDEBARAN_LON = 69.8   # α Tau
HYADES_LON = 66.0      # центр Гиад (ε Tau)

# Склонение J2000 (для видимости)
PLEIADES_DEC = 24.1
ALDEBARAN_DEC = 16.5

# Предполагаемая дата росписи: ~15 000 до н.э. (17 000 лет назад)
TARGET_YEAR = -15000

# Прецессия: за T лет эклиптические долготы были на PRECESSION*T° меньше
# (точка равноденствия была сдвинута)
def ecl_lon_at(lon_j2000, year):
    """Тропическая эклиптическая долгота звезды в заданную эпоху."""
    dt = year - 2000
    # Прецессия сдвигает точку равноденствия, звёзды остаются (в сидерич.)
    # Тропическая долгота = сидерическая - прецессия
    # Но мы работаем с тропическими J2000, значит:
    # lon(t) = lon_j2000 - PRECESSION * (2000 - t) [отматываем назад]
    return (lon_j2000 - PRECESSION * (2000 - year)) % 360

# Наклон эклиптики меняется: ~22.1° - 24.5° (цикл ~41 000 лет)
def obliquity_at(year):
    """Приблизительный наклон эклиптики."""
    T = (year - 2000) / 100
    # Простая модель (линейная + синусоидальная)
    return 23.44 - 0.013 * T  # грубо, но для оценки достаточно

# Экваториальные координаты из эклиптических (приблизительно)
def ecl_to_eq_dec(ecl_lon, ecl_lat, obliquity):
    """Склонение из эклиптических координат."""
    eps = np.radians(obliquity)
    lam = np.radians(ecl_lon)
    bet = np.radians(ecl_lat)
    sin_dec = np.sin(bet)*np.cos(eps) + np.cos(bet)*np.sin(eps)*np.sin(lam)
    return np.degrees(np.arcsin(np.clip(sin_dec, -1, 1)))

print("=" * 70)
print("  P1: Ласко — звёздный бык палеолита (~17 000 лет назад)")
print("=" * 70)
print()
print(f"  Пещера: Ласко, Дордонь, Франция ({LAT}°N, {LON}°E)")
print(f"  Предполагаемая дата: ~{abs(TARGET_YEAR)} г. до н.э.")
print(f"  Лет назад от 2026: {2026 - TARGET_YEAR}")
print()

# 1. Взаимное положение Плеяд и Альдебарана
# В J2000: Плеяды (60°) — Альдебаран (69.8°) — разница ~10°
# Это неизменно (собственные движения за 17000 лет малы для далёких звёзд)
sep_pleiades_aldebaran = ALDEBARAN_LON - PLEIADES_LON
print(f"  1. ВЗАИМНОЕ ПОЛОЖЕНИЕ (инвариант)")
print(f"     Плеяды — Альдебаран: {sep_pleiades_aldebaran:.1f}° (сейчас и 17 000 лет назад)")
print(f"     Плеяды всегда «над плечом» Тельца — рисунок согласуется ✓")
print()

# 2. Видимость над Ласко в ту эпоху
obl_then = obliquity_at(TARGET_YEAR)
pleiades_dec_then = ecl_to_eq_dec(ecl_lon_at(PLEIADES_LON, TARGET_YEAR), 0, obl_then)
aldebaran_dec_then = ecl_to_eq_dec(ecl_lon_at(ALDEBARAN_LON, TARGET_YEAR), 0, obl_then)

print(f"  2. ВИДИМОСТЬ НАД ЛАСКО ({LAT}°N)")
print(f"     Наклон эклиптики в {abs(TARGET_YEAR)} до н.э.: ~{obl_then:.1f}°")
print(f"     Склонение Плеяд тогда:    ~{pleiades_dec_then:.1f}°")
print(f"     Склонение Альдебарана:     ~{aldebaran_dec_then:.1f}°")
max_alt_pleiades = 90 - LAT + pleiades_dec_then
max_alt_aldebaran = 90 - LAT + aldebaran_dec_then
print(f"     Макс. высота Плеяд:        ~{max_alt_pleiades:.1f}°")
print(f"     Макс. высота Альдебарана:  ~{max_alt_aldebaran:.1f}°")
visible_pleiades = max_alt_pleiades > 0
visible_aldebaran = max_alt_aldebaran > 0
print(f"     Видны? Плеяды: {'ДА ✓' if visible_pleiades else 'НЕТ ✗'}, "
      f"Альдебаран: {'ДА ✓' if visible_aldebaran else 'НЕТ ✗'}")
print()

# 3. В каком сезоне Плеяды были видны вечером?
# Гелиакический восход (первая видимость перед рассветом) зависит от
# эклиптической долготы Солнца. Плеяды видны вечером, когда Солнце
# на ~180° от них.
pleiades_ecl_then = ecl_lon_at(PLEIADES_LON, TARGET_YEAR)
sun_opposite = (pleiades_ecl_then + 180) % 360
# Солнце на ~0° = весеннее равноденствие (март)
# Солнце на sun_opposite → какой месяц?
month_approx = (sun_opposite / 30) + 1  # грубо: 0°=март, 30°=апрель...
months = ['мар', 'апр', 'мая', 'июн', 'июл', 'авг', 'сен', 'окт', 'ноя', 'дек', 'янв', 'фев']
month_name = months[int(month_approx - 3) % 12]

print(f"  3. СЕЗОН НАБЛЮДЕНИЯ")
print(f"     Эклиптическая долгота Плеяд тогда: ~{pleiades_ecl_then:.0f}°")
print(f"     Видны вечером, когда Солнце на ~{sun_opposite:.0f}°")
print(f"     Это примерно: {month_name}")
print()

# 4. Ключевой вопрос: куда указывала ось «Плеяды—Альдебаран—Гиады»?
# Сейчас: почти горизонтально (обе ~+20° склонения, долготы 60-70°)
# 17 000 лет назад: прецессия повернула всё на ~237°
# Но ВЗАИМНОЕ положение звёзд в созвездии не меняется!
# Меняется ОРИЕНТАЦИЯ созвездия на небе (из-за прецессии оси Земли)
total_prec = PRECESSION * (2026 - TARGET_YEAR)
full_turns = total_prec / 360
print(f"  4. ПРЕЦЕССИЯ ЗА {2026-TARGET_YEAR} ЛЕТ")
print(f"     Полная прецессия: {total_prec:.0f}° = {full_turns:.2f} полных оборота")
print(f"     Остаток: {total_prec % 360:.0f}° сдвига от текущего положения")
print(f"     Созвездие Тельца выглядело ТАК ЖЕ (звёзды не разбежались)")
print(f"     Но было в ДРУГОЙ части неба (повёрнуто на {total_prec % 360:.0f}°)")
print()

# 5. Собственные движения за 17 000 лет
# Плеяды: ~400 пк, pm ~20 mas/yr → 17000 * 0.02"/yr = 340" = 5.7'
# Альдебаран: ~20 пк, pm ~200 mas/yr → 17000 * 0.2"/yr = 3400" = 56.7' ≈ 1°
pm_pleiades_total = 17000 * 20 / 1000 / 3600  # градусов
pm_aldebaran_total = 17000 * 200 / 1000 / 3600  # градусов

print(f"  5. СОБСТВЕННЫЕ ДВИЖЕНИЯ (изменение рисунка)")
print(f"     Плеяды (~400 пк, далеко):     сдвиг {pm_pleiades_total:.2f}° за 17 000 лет")
print(f"     Альдебаран (~20 пк, близко):   сдвиг {pm_aldebaran_total:.2f}° за 17 000 лет")
print(f"     Альдебаран сдвинулся на ~1° — заметно, но рисунок узнаваем ✓")
