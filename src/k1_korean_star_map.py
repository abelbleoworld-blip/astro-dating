print()

# Метод 1: Прецессия равноденственной точки
# Исследователи (Park 1998): отклонение равноденствия ~4-6° от J2000
equinox_offsets = [
    {'author': 'Rufffle & Stephenson 1988', 'offset_deg': 5.0, 'epoch': None},
    {'author': 'Park Changbom 1998', 'offset_deg': 4.5, 'epoch': None},
    {'author': 'Ahn Young-Sook 2010', 'offset_deg': 3.8, 'epoch': None},
]

print("  МЕТОД 1: Прецессия точки равноденствия")
print()
print(f"  {'Исследователь':<30} {'Δ равноденствия':>16} {'Эпоха':>10} {'До н.э./н.э.':>14}")
print("  " + "-" * 72)

for e in equinox_offsets:
    epoch = 2000 - e['offset_deg'] / PRECESSION
    e['epoch'] = epoch
    if epoch < 0:
        era = f"{abs(epoch)+1:.0f} до н.э."
    else:
        era = f"{epoch:.0f} н.э."
    print(f"  {e['author']:<30} {e['offset_deg']:>14.1f}° {epoch:>10.0f} {era:>14}")

mean_epoch = np.mean([e['epoch'] for e in equinox_offsets])
print(f"\n  Среднее: {mean_epoch:.0f} г. {'н.э.' if mean_epoch > 0 else 'до н.э.'}")
print(f"  Лет назад от 2026: {2026 - mean_epoch:.0f}")

print()

# Метод 2: Расстояние полюса мира от Полярной
# На карте центр (полюс) смещён от позиции Полярной
# Измеренное смещение на карте: ~6-8° (разные исследователи)
print("  МЕТОД 2: Расстояние полюса мира от Полярной звезды")
print()

measured_dist = [6.0, 7.5, 8.0]  # градусов, разные измерения

print(f"  Измеренное расстояние на карте: {min(measured_dist)}°–{max(measured_dist)}°")
print()
print(f"  {'Расст. на карте':>16} {'Соотв. эпоха':>14} {'Эра':>14}")
print("  " + "-" * 48)

for d in measured_dist:
    # Ищем, когда расстояние полюс-Полярная = d
    best_year = None
    best_diff = 999
    for y in range(-500, 2000, 10):
        dist = pole_position(y)
        if abs(dist - d) < best_diff:
            best_diff = abs(dist - d)
            best_year = y
    if best_year is not None:
        era = f"{best_year} н.э." if best_year > 0 else f"{abs(best_year)+1} до н.э."
        print(f"  {d:>14.1f}° {best_year:>14} {era:>14}")

print()

# Метод 3: Сравнение с Альмагестом
print("  МЕТОД 3: Сравнение с другими каталогами")
print()
print(f"  {'Каталог':<30} {'Звёзд':>7} {'Эпоха':>14} {'Метод датировки':<25}")
print("  " + "-" * 78)
