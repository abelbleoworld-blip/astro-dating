"""K1: Корейская звёздная карта Чхонсан Ёльча Пунья Чидо (1395).
Надпись: скопирована с оригинала эпохи Когурё (I–V вв. н.э.).
Метод: прецессионная датировка как Альмагест (Дамбис)."""

import numpy as np
from pathlib import Path
from datetime import datetime

OUT = Path(__file__).resolve().parent.parent / "results" / "k1_korean_star_map.md"
PRECESSION = 50.29 / 3600  # °/год

# Чхонсан Ёльча Пунья Чидо (天象列次分野之圖)
# 1467 звёзд, 264 астеризма, высечена в камне 1395 г.
# Надпись: оригинал времён Когурё, утрачен при падении в реку,
# восстановлен при Чосон по рубингу/копии.

# Ключевые звёзды карты с их положениями
# Координаты на карте — в системе «чи ду» (赤度, экваториальные)
# Полюс карты = северный полюс эклиптики (не небесный полюс!)
# Это КРИТИЧЕСКАЯ деталь — карта привязана к эклиптике, не к экватору.

# Для датировки используем ПОЛОЖЕНИЕ ПОЛЮСА МИРА на карте.
# Если карта фиксирует положение полюса — прецессия даёт дату.

# На карте полюс мира отмечен. Его координаты в эклиптической системе
# меняются из-за прецессии (полюс мира описывает круг за ~25772 года).

# Исторический анализ (Рюффл, Стивенсон 1988; Пак Чанбом 1998):
# Координаты полюса на карте соответствуют эпохе ~1°–2° н.э.
# (т.е. оригиналу Когурё, не копии 1395 г.)

# Альтернативный метод: положение точки весеннего равноденствия.
# На карте отмечен «красный путь» (эклиптика) и точка пересечения
# с «жёлтым путём» (небесный экватор). Эта точка = равноденствие,
# и она прецессирует.

# Данные из исследований карты (Park 1998, Rufffle & Stephenson 1988):
# Равноденствие на карте: RA ≈ 355° (в J2000 = 0° по определению)
# Разница = 5° → 5° / 0.01397°/год = 358 лет → ~1643 г. от J2000 → ~357 г. н.э.
# Но разные исследователи дают разные значения (погрешность чтения камня)

# Более надёжный метод: положение Полярной звезды.
# На карте центр НЕ совпадает с α UMi (Полярная).
# Расстояние полюса карты от Полярной → эпоха.

# α UMi (Полярная): текущее расстояние от полюса ~0.7°
# В I в. н.э.: ~12° от полюса
# В V в. н.э.: ~8° от полюса
# В XIV в. (1395): ~3.5° от полюса

# Полюс мира описывает круг вокруг полюса эклиптики
# RA полюса эклиптики ≈ 270°, Dec ≈ 66.56°
# Радиус круга = наклон эклиптики ≈ 23.44°

def pole_position(year):
    """Приблизительные координаты полюса мира (RA, Dec не нужны,
    нужно расстояние от α UMi)."""
    # α UMi J2000: RA=37.95°, Dec=89.26°
    # Полюс мира в эпоху year:
    # Угол прецессии от J2000
    angle = 2 * np.pi * (year - 2000) / 25772
    # Полюс мира (приблизительно):
    pole_ra = 270 + np.degrees(angle) * np.cos(np.radians(66.56))
    pole_dec = 90 - 23.44  # это неправильно, Dec полюса = 90° всегда
    # На самом деле расстояние полюс-Полярная:
    # Полярная: Dec=89.26° → расстояние от полюса = 90-89.26 = 0.74° (J2000)
    # Из-за прецессии Полярная «уходит» от полюса
    # Собственное движение Полярной мало (pm ~45 mas/yr)
    # Расстояние меняется в основном из-за прецессии

    # Грубая модель: Полярная была ближе всего к полюсу ~2105 г. н.э.
    # и удалялась назад в прошлое со скоростью ~20"/столетие
    T = (2105 - year) / 100  # столетий от ближайшего сближения
    dist = 0.5 + 0.003 * T**2  # грубая параболическая модель (градусы)
    return min(dist, 25)  # не больше 25°

