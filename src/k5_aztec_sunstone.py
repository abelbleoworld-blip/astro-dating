matched = 0
for az in eclipses_aztec:
    best_match = None
    best_delta = 999
    for n in nasa_eclipses_mesoamerica:
        if abs(n['year'] - az['year']) <= 1:
            delta_days = abs((n['month']*30+n['day']) - (az['month']*30+az['day']))
            if delta_days < best_delta:
                best_delta = delta_days
                best_match = n
    if best_match and best_delta < 60:
        matched += 1
        print(f"     {az['year']:>6} {az['month']:>6}/{az['day']:<8} {best_match['month']:>6}/{best_match['day']:<8} {best_delta:>8} {best_match['type']:>10}")
    else:
        print(f"     {az['year']:>6} {az['month']:>6}/{az['day']:<8} {'—':>15} {'—':>10} {'—':>10}")

print(f"\n     Совпадений: {matched}/{len(eclipses_aztec)}")
