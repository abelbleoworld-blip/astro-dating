for s in FIVE_SUNS:
    dur = s['duration_years'] or 0
    x52 = s['duration_x52'] or '-'
    total_duration += dur
    print(f"     {s['name']:<22} {s['element']:<20} {dur if dur else '—':>6} {x52:>5}")

