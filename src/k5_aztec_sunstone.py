# === 1. Календарная система ===
print("  1. КАЛЕНДАРНАЯ СИСТЕМА")
print()

# Тональпоуалли (ритуальный): 260 дней = 13 × 20
# Шиупоуалли (солнечный): 365 дней = 18 × 20 + 5 немонтеми
# Календарный круг: НОК(260, 365) = 18 980 дней = 52 года по 365

TONALPOHUALLI = 260  # ритуальный цикл
XIUHPOHUALLI = 365   # солнечный год
CALENDAR_ROUND = np.lcm(TONALPOHUALLI, XIUHPOHUALLI)
CALENDAR_ROUND_YEARS = CALENDAR_ROUND / XIUHPOHUALLI

print(f"     Тональпоуалли (ритуальный):   {TONALPOHUALLI} дней = 13 × 20")
print(f"     Шиупоуалли (солнечный):        {XIUHPOHUALLI} дней = 18 × 20 + 5")
print(f"     Календарный круг:              {CALENDAR_ROUND} дней = {CALENDAR_ROUND_YEARS:.0f} лет")
print()

# 260 = астрономическое число?
# 260 ≈ период видимости Венеры как утренней звезды (~263 дня)
# 260 ≈ период от посева до сбора маиса на 15°N (~260 дней)
# 260 ≈ период беременности человека (~266 дней)
# 260 = 13 × 20 (13 = суставы 4 конечностей, 20 = пальцы рук+ног)
print(f"     Число 260 — возможные источники:")
print(f"       Венера (видимость утром):  ~263 дня (ошибка 1.2%)")
print(f"       Маис (посев→сбор, 15°N):  ~260 дней")
print(f"       Беременность:              ~266 дней (ошибка 2.3%)")
print(f"       Анатомия: 13 × 20          (суставы × пальцы)")
print()

# === 2. Венерианский цикл ===
print("  2. ВЕНЕРИАНСКИЙ ЦИКЛ")
print()

VENUS_SYNODIC_MODERN = 583.9  # дней (современное значение)
VENUS_AZTEC = 584  # дней (ацтекское значение)
venus_error = abs(VENUS_AZTEC - VENUS_SYNODIC_MODERN) / VENUS_SYNODIC_MODERN * 100

# 5 циклов Венеры = 8 солнечных лет = 2920 дней
VENUS_5 = 5 * VENUS_AZTEC  # 2920
SOLAR_8 = 8 * XIUHPOHUALLI  # 2920
match_5_8 = VENUS_5 == SOLAR_8

# Связь с Дрезденским кодексом (Майя)
VENUS_DRESDEN = 584  # тот же цикл!

print(f"     Ацтекский период Венеры:    {VENUS_AZTEC} дней")
print(f"     Современное значение:       {VENUS_SYNODIC_MODERN} дней")
print(f"     Ошибка:                     {venus_error:.2f}%")
print(f"     5 Венер = 8 солнечных лет:  {VENUS_5} = {SOLAR_8} → {'✓' if match_5_8 else '✗'}")
print(f"     Дрезденский кодекс (Майя):  {VENUS_DRESDEN} дней → ИДЕНТИЧНО")
