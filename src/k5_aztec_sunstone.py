print(f"     4 прямоугольника вокруг центра = 4 предыдущих Солнца (эпохи)")
print()

# Пять Солнц (эпох)
FIVE_SUNS = [
    {'name': 'Нахуи-Оцелотль',   'element': 'Земля (ягуары)',    'end': 'Ягуары сожрали людей',
     'duration_years': 676, 'duration_x52': 13},
    {'name': 'Нахуи-Ээкатль',    'element': 'Ветер',              'end': 'Ураганы превратили людей в обезьян',
     'duration_years': 364, 'duration_x52': 7},
    {'name': 'Нахуи-Киауитль',   'element': 'Огненный дождь',     'end': 'Огненный дождь',
     'duration_years': 312, 'duration_x52': 6},
    {'name': 'Нахуи-Атль',       'element': 'Вода (потоп)',        'end': 'Всемирный потоп',
     'duration_years': 676, 'duration_x52': 13},
    {'name': 'Нахуи-Олин',       'element': 'Движение (землетрясения)', 'end': 'Текущая эпоха',
     'duration_years': None, 'duration_x52': None},
]

print(f"     Пять Солнц (космических эпох):")
print(f"     {'Солнце':<22} {'Элемент':<20} {'Лет':>6} {'×52':>5}")
